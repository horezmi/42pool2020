#include <unistd.h>

int		main(int argc, char *argv[])
{
	int i;
	int n;

	n = argc;
	while (n > 1)
	{
		i = 0;
		while (argv[n - 1][i] != '\0')
		{
			write(1, &argv[n - 1][i], 1);
			i++;
		}
		write(1, "\n", 1);
		n--;
	}
	return (0);
}
